package com.matrix.pojo.model;

// 选项
public class Option {
	private String tip; // 选项: A|B|C|D
	private String option; // 选项对应内容;
	
	public String getTip() {
		return tip;
	}
	public void setTip(String tip) {
		this.tip = tip;
	}
	public String getOption() {
		return option;
	}
	public void setOption(String option) {
		this.option = option;
	}
}
